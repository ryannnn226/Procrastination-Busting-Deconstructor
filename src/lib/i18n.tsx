interface I18nContextType { lang: Lang; setLang: (l: Lang) => void; t: (k: string, ...args: string[]) => string }
const I18nContext = createContext<I18nContextType>({ lang: 'zh', setLang: () => {}, t: (k: string) => k })

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => { const s = localStorage.getItem('app-lang'); return s === 'en' ? 'en' : 'zh' })
  useEffect(() => { localStorage.setItem('app-lang', lang); document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en' }, [lang])
  const t = useCallback((key: string, ...args: string[]): string => {
    const e = translations[key]
    let result = e ? (e[lang] ?? e['zh'] ?? key) : key
    for (let i = 0; i < args.length; i++) {
      result = result.replace('{' + i + '}', args[i])
    }
    return result
  }, [lang])
  return (<I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>)
}

export function useT() { return useContext(I18nContext) }
